public class AreaCircunferencia {

	public static void main(String[] args) {
		double areaCircunferenciaRaioDez = Math.PI * Math.pow(10, 2);
		System.out.printf("Area da circunferencia: %.1f", areaCircunferenciaRaioDez);
	}

}
